// Iterate Odd Numbers With a For Loop...


var ourArray = [];
                                   /* Even numbers from 0 - 10 array will contain [2,4,6,8] */
for (var i = 0; i < 10; i += 2) {
  ourArray.push(i);
}


var myArray = [];

                                   /* Odd numbers from 1 - 9 array will contain [1,3,5,7,9] */
for (var i = 1; i < 10; i += 2){
  myArray.push(i);
}
