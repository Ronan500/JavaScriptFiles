// Count Backwards With For Loop....

var ourArray = [];
                                  /* Here the output in the array  will be [10,8,6,4,2,] */
for (var i = 10; i > 0; i -= 2) {
  ourArray.push(i);
}


var myArray = [];

                                    /* Here the output in the array  will be [9,7,5,3,1]  */
for (var i = 9; i > 0; i -= 2){
  myArray.push(i);
}
